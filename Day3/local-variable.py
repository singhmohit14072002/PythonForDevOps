def add():
  """
  Function to add two numbers, using local variables for the operands and result.
  """

  num1 = 10  # this the local declared variable, so this only will be used within the function
  num2 = 5
  result = num1 + num2
  print("Add", result)

def subtract():
  """
  Function to subtract one number from another, using local variables for the operands and result.
  """
  num1 = 10
  num2 = 3
  result = num1 - num2
  print("subtract", result)


add()
subtract()