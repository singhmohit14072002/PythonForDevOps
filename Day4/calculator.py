def addition(num1, num2):
    add = num1 + num2
    return add

def subtraction(num1, num2):
    s = num1 - num2
    return s

def multiplication(num1, num2):
    mul = num1 * num2
    return mul

def division(num1, num2):
    dev = num1 / num2
    return dev


print(addition(5,100))
print(subtraction(100,23))
print(multiplication(1000,5000))
